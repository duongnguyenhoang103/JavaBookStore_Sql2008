    private void btnAddAuthorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddAuthorMouseClicked
        // TODO add your handling code here:
        if (tfAddAuthor.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Name is empty!");
            return;
        }
        AuthorService authorS = new AuthorService();
        int k = authorS.addAuthor(new Author(0, tfAddAuthor.getText()));
        if (k > 0) {
            loadAuthor();
        }
    }//GEN-LAST:event_btnAddAuthorMouseClicked

    private void tblAuthorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAuthorMouseClicked
        // TODO add your handling code here:
        int a = tblAuthor.getSelectedRow();
        if (a == -1) {
            return;
        }
        Author author = (Author) tblAuthor.getValueAt(a, 1);
        tfEditAuthorID.setText(Integer.toString(author.getAuthorId()));
        tfEditAuthorName.setText(author.getAuthorName());
    }//GEN-LAST:event_tblAuthorMouseClicked

    private void btnSaveAuthorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveAuthorMouseClicked
        // TODO add your handling code here:
        int id = 0;
        try {
            id = Integer.parseInt(tfEditAuthorID.getText());
        } catch (Exception e) {
            return;
        }
        AuthorService authorS = new AuthorService();
        int k = authorS.editAuthor(new Author(id, tfEditAuthorName.getText()));
        if (k > 0) {
            loadAuthor();
        }
    }//GEN-LAST:event_btnSaveAuthorMouseClicked

    private void btnDeleteAuthorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteAuthorMouseClicked
        // TODO add your handling code here:
        int[] a = tblAuthor.getSelectedRows();
        if (a.length == 0) {
            return;
        }
        AuthorService authS = new AuthorService();
        for (int i = 0; i < a.length; i++) {
            authS.deleteAuthor((Author) tblAuthor.getValueAt(a[i], 1));
        }
        loadAuthor();
    }//GEN-LAST:event_btnDeleteAuthorMouseClicked
