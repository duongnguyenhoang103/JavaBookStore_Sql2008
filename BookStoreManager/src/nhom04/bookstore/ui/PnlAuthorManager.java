    private void loadAuthor() {
        AuthorService authorS = new AuthorService();
        Vector<Author> authorV = authorS.getListAuthor();
        DefaultTableModel model = new DefaultTableModel();
        DefaultTableModel oldModel = (DefaultTableModel) tblAuthor.getModel();
        int n = oldModel.getColumnCount();
        for (int i = 0; i < n; i++) {
            model.addColumn(oldModel.getColumnName(i));
        }
        n = authorV.size();
        for (int i = 0; i < n; i++) {
            model.addRow(new Object[]{authorV.get(i).getAuthorId(), authorV.get(i)});
        }
        tblAuthor.setModel(model);
    }

