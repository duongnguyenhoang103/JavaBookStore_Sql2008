    private void btnAddCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddCategoryMouseClicked
        // TODO add your handling code here:
        if (tfAddCategory.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Name empty!");
            return;
        }
        Category cat = new Category();
        cat.setCategoryName(tfAddCategory.getText());
        CategoryService catS = new CategoryService();
        int k = catS.addCategory(cat);
        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Them loai sach thanh cong!");
            loadCategory();
        } else {
            JOptionPane.showMessageDialog(this, "fail");
        }
    }//GEN-LAST:event_btnAddCategoryMouseClicked

    private void tblCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoryMouseClicked
        // TODO add your handling code here:
        int c = tblCategory.getSelectedRow();
        if (c == -1) {
            return;
        }
        Category cat = (Category) tblCategory.getValueAt(c, 1);

        tfEditCategoryID.setText(Integer.toString(cat.getCategoryId()));
        tfEditCategoryName.setText(cat.getCategoryName());
    }//GEN-LAST:event_tblCategoryMouseClicked

    private void btnSaveCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveCategoryMouseClicked
        // TODO add your handling code here:
        int id = 0;
        try {
            id = Integer.parseInt(tfEditCategoryID.getText());
        } catch (Exception e) {
            return;
        }
        Category cat = new Category(id, tfEditCategoryName.getText());
        CategoryService catS = new CategoryService();
        int k = catS.editCategory(cat);
        if (k > 0) {
            loadCategory();
        }
    }//GEN-LAST:event_btnSaveCategoryMouseClicked

    private void btnDeleteCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteCategoryMouseClicked
        // TODO add your handling code here:
        int[] c = tblCategory.getSelectedRows();
        if (c.length == 0) {
            return;
        }
        CategoryService catS = new CategoryService();
        for (int i = 0; i < c.length; i++) {
            Category cat = (Category) tblCategory.getValueAt(c[i], 1);
            catS.deleteCategory(cat);
        }
        loadCategory();
    }//GEN-LAST:event_btnDeleteCategoryMouseClicked
