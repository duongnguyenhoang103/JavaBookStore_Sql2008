    private void loadCategory() {
        CategoryService catS = new CategoryService();
        Vector<Category> catV = catS.getListCategory();
        DefaultTableModel model = new DefaultTableModel();
        DefaultTableModel oldModel = (DefaultTableModel) tblCategory.getModel();
        int n = oldModel.getColumnCount();
        for (int i = 0; i < n; i++) {
            model.addColumn(oldModel.getColumnName(i));
        }
        n = catV.size();
        for (int i = 0; i < n; i++) {
            model.addRow(new Object[]{catV.get(i).getCategoryId(), catV.get(i)});
        }
        tblCategory.setModel(model);
    }

